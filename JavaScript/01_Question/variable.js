//Primitive Data Types/ Values

//string data type

let str = "Nishant";
console.log("My name is", str);
console.log(typeof(str));

//boolean data type
const bool = true;
const bool2 = false;

console.log("\nBoolean Values are:", bool, bool2);
console.log(typeof(bool));

//undefined data type
let undef;//default type of undef is undefined not its value, as no value has been assigned
console.log("\nVariable undef is", undef);//value is undefined
console.log("Type of Variable undef is", typeof(undef));//type is undefined

//null data type
let n = null;//
console.log("\nValue of Variable n is", n);//value is null
console.log("Type of Variable n is", typeof(n));//variable n is some object

//number
let a = 1;
console.log("\nVariable a is", a);
console.log("Type of Variable a is", typeof(a));
